function getChapters(syllabus, userClass, term) {
  if (syllabus === 'samacheer') {
    const classData = samacheerKalvi[userClass];
    if (!classData) return null;
    if (term) {
      return classData[term] || null;
    }
    return classData;
  } else if (syllabus === 'cbse') {
    return cbse[userClass] || null;
  }
  return null;
}

function getAvailableClasses(syllabus) {
  if (syllabus === 'samacheer') {
    return Object.keys(samacheerKalvi);
  } else if (syllabus === 'cbse') {
    return Object.keys(cbse);
  }
  return [];
}

